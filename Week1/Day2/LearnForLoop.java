package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		
	
		for(int i=2;i<=5;i++) {
			System.out.println(i);
				}
		
		System.out.println("--------------");
		
		//Reverse for loop
		//Higher value to lower value
		//Higher value is 5, lower value is 1
		//5  4 3  2 1
		for(int i=5;i>=1;i--) {
			System.out.println(i);
		}

	}

}
