public class PrintEvenNumberFrom1to10 {

	public static void main(String[] args) {
		//Identify even numbers and odd numbers
		
		for (int i = 1; i <=10; i++) {
			
		if(i%2==0) {
				System.out.println(i);
			}
			
		}

	}

}
